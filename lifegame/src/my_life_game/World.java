	 private int rows = world.length;
	 private int cols = world[0].length;
	 private int[][] nextmap = new int[rows][cols];
	 private int[][] nextnextmap = new int[rows][cols];
	 private int leng = 13;
	 private Timer timer;
	 
	 public World() {  
		 rows = world.length;
		 cols = world[0].length;
		 nextmap = new int[rows][cols];
		 nextnextmap = new int[rows][cols];
		 for (int i = 0; i < rows; i++) {  
	            for (int j = 0; j < cols; j++) {  
	                nextmap[i][j] = world[i][j];  
	                nextnextmap[i][j] = world[i][j];  
	            }  
	        }  
	        this.startAnimation();  
	        
	 }  
	 public World(int[][] m) {  
		 rows=m.length;
		 cols=m[0].length;
		 nextmap = new int[rows][cols];
		 nextnextmap = new int[rows][cols];
		 for (int i = 0; i < m.length; i++) {  
	            for (int j = 0; j < m[i].length; j++) {  
	                nextmap[i][j] = m[i][j];  
	                nextnextmap[i][j] = m[i][j];  
	            }  
	     }  
	     this.startAnimation();
	 }  
	 private void startAnimation(){
		 timer = new Timer(120, new ActionListener() {		
			@Override
			public void actionPerformed(ActionEvent e) {
				// TODO Auto-generated method stub
				changemap();
				repaint();
			}
		});
		timer.start(); 
	 }
	 
	 @Override
	 protected void paintComponent(Graphics g) {
		super.paintComponent(g);  
		for(int i=0;i<rows;i++){
			for(int j=0;j<cols;j++){
				if(nextmap[i][j]==0){
					g.drawRect( j * leng, i * leng, leng, leng); 
				}
				else{
					g.fillRect( j * leng, i * leng, leng, leng); 
				}
			}
		 }
	 }
	 public int[][] returnMap()
	 {
		 return nextmap;
	 }
	 public void changemap(){
		 for(int i=0;i<rows;i++){
			 for(int j=0;j<cols;j++){
				 int num = getnext(i, j);
				 if(num==0||num==1||num==4||num==5||num==6||num==7
						 ||num==8){
					 nextmap[i][j] = 0;
				 }
				 else if(num==2){
					 nextmap[i][j] = nextnextmap[i][j];
				 }
				 else if(num==3){
					 nextmap[i][j] = 1;
				 }
			 }
		 }
		 
		 for(int i=0;i<rows;i++){
			 for(int j=0;j<cols;j++){
				 nextnextmap[i][j] = nextmap[i][j];
			 }
		 }
	 }
	 
	 
	 
	 private int getnext(int row,int col){
		 int num = 0;
		 for(int i=row-1;i<=row+1;i++){
			 for(int j=col-1;j<=col+1;j++){
				 if (i < 0 || i >= rows ||j < 0  
	                        || j >= cols||(i==row&&j==col)) {  
	                    continue;  
	                }  
	                if (nextnextmap[i][j] == 1) {  
	                    num++;            
	                }  
			 }
		 }
		
		 return num;
	 }
	

